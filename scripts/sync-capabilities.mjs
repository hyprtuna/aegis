#!/usr/bin/env node
// sync-capabilities.mjs — regenerate the cross-host capability docs from
// manifest/capabilities.json (the single source of truth, locked in v0.0.5).
//
// Generates TWO files:
//   - docs/capability-matrix.md  — fully generated; rows = capabilities,
//     columns = the 5 hosts, cells = status + short evidence.
//   - docs/harnesses.md          — host-status view; only the block between
//     the AEGIS:CAPABILITIES markers is regenerated. Hand-written prose around
//     the markers is preserved. If the file has no markers (or doesn't exist),
//     it is created/scaffolded with a generated block and a stub prose intro.
//
// Flags:
//   (none)    rewrite both files in place.
//   --check   regenerate in memory, diff against committed files; exit 1 on
//             drift (printing a line-level summary), exit 0 when in sync.
//
// Node 20+ stdlib only. Idempotent: a second run changes nothing.

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";

const REPO = new URL("..", import.meta.url).pathname.replace(/\/$/, "");
const CAPS_PATH = join(REPO, "manifest", "capabilities.json");
const MATRIX_PATH = join(REPO, "docs", "capability-matrix.md");
const HARNESSES_PATH = join(REPO, "docs", "harnesses.md");

const HOSTS = ["claude", "opencode", "codex", "cursor", "zed"];
const HOST_LABELS = {
  claude: "Claude Code",
  opencode: "OpenCode",
  codex: "Codex",
  cursor: "Cursor",
  zed: "Zed",
};
const STATUS_ICON = {
  supported: "✅ supported",
  partial: "🟡 partial",
  gap: "⛔ gap",
  "n/a": "— n/a",
};

const GEN_NOTICE =
  "<!-- GENERATED by scripts/sync-capabilities.mjs — do not edit. Source: manifest/capabilities.json -->";
const MARK_START = "<!-- AEGIS:CAPABILITIES:START -->";
const MARK_END = "<!-- AEGIS:CAPABILITIES:END -->";

function loadCaps() {
  const parsed = JSON.parse(readFileSync(CAPS_PATH, "utf8"));
  if (!Array.isArray(parsed.capabilities)) {
    throw new Error("manifest/capabilities.json: missing `capabilities` array");
  }
  return parsed.capabilities;
}

// Cell text for one host on one capability: status icon + short evidence.
function cell(host) {
  if (!host) return STATUS_ICON["n/a"];
  const status = host.status || "n/a";
  const icon = STATUS_ICON[status] || `? ${status}`;
  const ev = host.evidence ? `<br/>\`${host.evidence}\`` : "";
  return icon + ev;
}

// docs/capability-matrix.md — full generated table.
function renderMatrix(caps) {
  const lines = [];
  lines.push(GEN_NOTICE);
  lines.push("");
  lines.push("# Capability Matrix");
  lines.push("");
  lines.push(
    "Cross-host status for every Aegis capability, generated from `manifest/capabilities.json`.",
  );
  lines.push(
    "Legend: ✅ supported · 🟡 partial · ⛔ gap · — n/a. Evidence cites a file path or section anchor.",
  );
  lines.push("");
  const header = ["Capability", ...HOSTS.map((h) => HOST_LABELS[h])];
  lines.push("| " + header.join(" | ") + " |");
  lines.push("|" + header.map(() => "---").join("|") + "|");
  for (const cap of caps) {
    const title = `**${cap.title}**<br/><sub>${cap.id}</sub>`;
    const cells = HOSTS.map((h) => cell(cap.hosts?.[h]));
    lines.push("| " + [title, ...cells].join(" | ") + " |");
  }
  lines.push("");
  // Per-capability detail: description + category, so the matrix is self-contained.
  lines.push("## Capability detail");
  lines.push("");
  for (const cap of caps) {
    lines.push(`### ${cap.title} (\`${cap.id}\`)`);
    lines.push("");
    lines.push(`*Category: ${cap.category || "—"}*`);
    lines.push("");
    lines.push(cap.description || "");
    lines.push("");
  }
  return lines.join("\n").trimEnd() + "\n";
}

// The generated block injected into docs/harnesses.md — host-status oriented:
// one section per host listing supported / partial / gap capabilities.
function renderHarnessesBlock(caps) {
  const lines = [];
  lines.push(MARK_START);
  lines.push(GEN_NOTICE);
  lines.push("");
  lines.push("## Capability status by host");
  lines.push("");
  lines.push(
    "Generated from `manifest/capabilities.json`. See [capability-matrix.md](capability-matrix.md) for the full grid.",
  );
  lines.push("");
  // Compact grid first (status icon only) for an at-a-glance read.
  const header = ["Capability", ...HOSTS.map((h) => HOST_LABELS[h])];
  lines.push("| " + header.join(" | ") + " |");
  lines.push("|" + header.map(() => "---").join("|") + "|");
  for (const cap of caps) {
    const cells = HOSTS.map((h) => {
      const s = cap.hosts?.[h]?.status || "n/a";
      return STATUS_ICON[s] || `? ${s}`;
    });
    lines.push("| " + [`\`${cap.id}\``, ...cells].join(" | ") + " |");
  }
  lines.push("");
  lines.push(MARK_END);
  return lines.join("\n");
}

// Scaffold harnesses.md when it doesn't exist or lacks markers.
function scaffoldHarnesses(block) {
  return [
    "# Harnesses",
    "",
    "Aegis projects one canonical content tree into five host harnesses: Claude Code,",
    "OpenCode, Codex, Cursor, and Zed. The table below tracks which cross-host",
    "capabilities each harness supports. It is generated — edit `manifest/capabilities.json`",
    "and run `node scripts/sync-capabilities.mjs`, never edit the generated block by hand.",
    "",
    block,
    "",
  ].join("\n");
}

// Replace the marked block in existing harnesses.md content; preserve the rest.
function injectHarnessesBlock(existing, block) {
  const startIdx = existing.indexOf(MARK_START);
  const endIdx = existing.indexOf(MARK_END);
  if (startIdx === -1 || endIdx === -1 || endIdx < startIdx) {
    // No usable markers: append a fresh generated block, keep all prose.
    const trimmed = existing.replace(/\s+$/, "");
    return trimmed + "\n\n" + block + "\n";
  }
  const before = existing.slice(0, startIdx);
  const after = existing.slice(endIdx + MARK_END.length);
  return before + block + after;
}

function buildHarnesses(caps) {
  const block = renderHarnessesBlock(caps);
  if (!existsSync(HARNESSES_PATH)) return scaffoldHarnesses(block);
  // File exists: regenerate the marked block, preserve all surrounding prose.
  // injectHarnessesBlock also handles the marker-absent case (appends a block).
  return injectHarnessesBlock(readFileSync(HARNESSES_PATH, "utf8"), block);
}

// Minimal line-level diff summary for --check.
function diffSummary(label, expected, actual) {
  const exp = expected.split("\n");
  const act = actual.split("\n");
  const max = Math.max(exp.length, act.length);
  const out = [`--- ${label} (committed)`, `+++ ${label} (regenerated)`];
  let shown = 0;
  for (let i = 0; i < max && shown < 20; i++) {
    if (exp[i] !== act[i]) {
      if (act[i] !== undefined) out.push(`- ${i + 1}: ${act[i]}`);
      if (exp[i] !== undefined) out.push(`+ ${i + 1}: ${exp[i]}`);
      shown++;
    }
  }
  if (shown === 20) out.push("  … (diff truncated)");
  return out.join("\n");
}

function main() {
  const check = process.argv.includes("--check");
  const caps = loadCaps();
  const matrix = renderMatrix(caps);
  const harnesses = buildHarnesses(caps);

  if (check) {
    let drift = false;
    for (const [path, expected, label] of [
      [MATRIX_PATH, matrix, "docs/capability-matrix.md"],
      [HARNESSES_PATH, harnesses, "docs/harnesses.md"],
    ]) {
      const actual = existsSync(path) ? readFileSync(path, "utf8") : "";
      if (actual !== expected) {
        drift = true;
        console.error(`DRIFT: ${label} is out of sync with manifest/capabilities.json`);
        console.error(diffSummary(label, expected, actual));
        console.error("");
      }
    }
    if (drift) {
      console.error("Run `node scripts/sync-capabilities.mjs` to regenerate.");
      process.exit(1);
    }
    console.log("capability docs in sync ✓");
    return;
  }

  writeFileSync(MATRIX_PATH, matrix, "utf8");
  writeFileSync(HARNESSES_PATH, harnesses, "utf8");
  console.log(`Wrote ${MATRIX_PATH}`);
  console.log(`Wrote ${HARNESSES_PATH}`);
}

main();
